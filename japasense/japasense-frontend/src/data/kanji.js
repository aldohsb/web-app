export const getKanjiByLevel = (level) => {
  return kanjiData.filter(item => item.level === level);
};

export const getRandomKanji = (count = 1, levels = ['N5', 'N4']) => {
  const filtered = kanjiData.filter(item => levels.includes(item.level));
  const shuffled = [...filtered].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

export const getKanjiByReading = (reading) => {
  return kanjiData.find(item => 
    item.reading.toLowerCase() === reading.toLowerCase() ||
    item.reading.split('/').some(r => r.toLowerCase() === reading.toLowerCase())
  );
};

